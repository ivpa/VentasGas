	public String getTIPO_PRODUCTO() {
		return TIPO_PRODUCTO;
	}
	public void setTIPO_PRODUCTO(String tIPO_PRODUCTO) {
		TIPO_PRODUCTO = tIPO_PRODUCTO;
	}
	public int getCANTIDAD() {
		return CANTIDAD;
	}
